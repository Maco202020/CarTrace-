		public static string GetDecodedValue(string pid, int a, int b, int c, int d)
		{
			if (FormuleDecodare.TryGetValue(pid, out var aplicaFormula))
			{
				return aplicaFormula(a, b, c, d);
			}
			return "Unknown PID or missing formula.";
		}
